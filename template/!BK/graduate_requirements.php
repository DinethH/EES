<h6>REQUIREMENTS FOR ENVIRONMENTAL AND EARTH SCIENCES DOCTORAL DEGREE</h6>
<P>The Doctoral Program provides students with the interdisciplinary knowledge and skills to conduct independent research in Environmental and Earth Sciences. Students conduct dissertation research under the supervision of a faculty member in one of the participating departments (Biology, Chemistry, Earth and Environmental Sciences, Civil and Environmental Engineering, or Urban and Public Affairs). The supervising professor and a faculty committee assign courses in this primary area of emphasis to support the student's research and professional goals. To provide interdisciplinary training, additional courses are assigned in a secondary area of emphasis.</P>
<P>If they have not already done so in their previous work, all Doctoral students must take two engineering courses; two or three science courses (two if their prior training is in science, three if in engineering or another non-science field); and one course in policy or planning.
Students who enter the Doctoral Program with a Master's degree in a science or engineering field, or with 30 semester hours of graduate coursework, take a Diagnostic Examination In the first year of residence to evaluate this previous work. The student's supervising committee must approve all courses taken to meet degree requirements.</P>
<p>Students who enter the Doctoral Program with a Bachelor's degree take 30 semester hours
of graduate coursework that includes Engineering, Science and Public Policy courses. These students are encouraged to take the diagnostic exam in their first year of enrollment. The student's supervising committee must approve all courses taken to meet degree requirements.</p>
<p>Students may choose among any of the five participating units for their primary and secondary areas of emphasis. Course selection within these areas of emphasis is guided by the student's supervising committee and must result in a cohesive program that supports the dissertation research.</p>

Other requirements include:
<ol>
	<li>Successful completion of the Diagnostic Examination at the end of the first year of residence.</li>
	<li>Successful completion of the Comprehensive Examination, an oral defense of a research proposal to be pursued for the dissertation, and a specialization examination over areas of the student's proposed research.</li>
	<li>Demonstration of proficiency in one foreign language or a research tool such as advanced computer skills, statistics, or operations research.</li>
	<li>Successful defense of the dissertation and acceptance of the dissertation by the supervising committee.</li>
</ol>

